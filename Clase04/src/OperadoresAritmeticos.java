
public class OperadoresAritmeticos {

	public static void main(String[] args) {
		
		// Literal
		int i1 = 1;
		int i2 = 2;
		long l1 = 100;
		long l2 = i1 + l1;
		int i3 = 5/2; // -> 2
		System.out.println("int = 5/2 = " + i3);
		double d1 = 5 / 2; // -> 2.0
		System.out.println(d1);
		double d2 = 5.0 / 2;  // -> 2.5
		System.out.println(d2);
		
		int i4 = 5 % 2; // ->1
		System.out.println(i4);
		
		int i5 = 1;
		 System.out.println(i5);
		 System.out.println(i5++);
		 System.out.println(i5);
		 System.out.println(++i5);
		 System.out.println(i5);
		
	}
	
}
